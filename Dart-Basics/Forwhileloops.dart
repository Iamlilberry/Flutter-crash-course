void main(){
  // for loop - used when you know how many times you want to repeat
  for(int i = 0; i <= 10; i++){
    print("Number $i");
  }
  // while loops - runs forever as condition is true
  int i = 0;
  while (i < 10){
    print(i);
    i++;
  }

  String password = "";
  while(password != "Berry123"){
    password = "Berry123";
    
  }
  print("Access granted");
}