//  ARITHMETIC OPERATORS
void main(){
  int a = 5;
  int b = 5;

  print(a + b); // add
  print(a - b); // subtract
  print(a * b); // multiply
  print(a / b); // division
  print(a ~/ b); // integer division
  print(a % b);  // remainder division

// ASSIGNMENT OPERATORS = assign or update values
int x = 20; // (=) assign i.e (+=) - add and assign 

x += 8;
x -= 6;
x ~/= 4;
x *= 2;

// COMPARION OPERATOR = used in if/else, loops, condition
int age = 18;

print(age == 18); // equal too
print(age <= 18); // less or equal
print(age >= 18); // greater or equal
print(age < 18); // less than
print(age > 18); // greater than
print(age != 18); // not equal too

// TYPE TEST OPERATORS = check data types
var x = 18;
print(x is String); // check type
print(x is! int); // is not

// LOGICAL OPERATOR = used in conditions
// && ==  AND
// ! == NOT
// || = OR

// INCREMENT AND DECREMENT OPERATORS - used in loops
// ++ = add 1 
// -- = subtract 1
// +=2 = add 2
// -= 2 = subtract 2 
}

